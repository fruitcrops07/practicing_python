# Python's variable declaration

# python is a dynamic typed language. a variable in python can hold
# any type of data unlike Java and C# which is strictly typed language.

# declaring a variable
variable = "a"
print(type(variable))
variable = 1
print(type(variable))
variable = 1.0
print(type(variable))

# by using the BIT type(), we can print the class of
# a variable